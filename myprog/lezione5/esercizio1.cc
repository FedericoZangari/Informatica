#include<iostream>
using namespace std;

int main(){
	
	for(int i=0; i<5; i++){
		cout << "Hello word for index = " << i<< endl;
		}
		
	int k=0;
	while(k<5){
		cout << "Hello word for index = " << k<< endl;
		k++;
	}
	
	int i=0;
	do{
		cout << "Hello word for index = " << i<< endl;
		i++;
		}while(i<5);
	
	
	
	return 0;
}
